def test_script():
    text = "my Name Is atiqur RAhman"

    def make_upper(text):
        u = text.upper()
        print(u)
    make_upper(text)

    def make_capital(text):
        c = text.capitalize()
        print(c)
    make_capital(text)

    def make_lower(text):
        l = text.lower()
        print(l)
    make_lower(text)


test_script()
